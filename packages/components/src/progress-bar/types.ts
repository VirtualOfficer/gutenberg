export type ProgressBarProps = {
	/**
	 * Value of the progress bar.
	 */
	value?: number;

	/**
	 * An optional width of the progress bar (in pixels) for the progress bar
	 * (track) element.
	 */
	width?: number;

	/**
	 * A CSS class to apply to the progress bar wrapper (track) element.
	 */
	className?: string;
};
